// =========================================================
// Translation Engine
// =========================================================

(function () {
    'use strict';

    const STORAGE_KEY = 'noshahi_lang';
    let currentLang = localStorage.getItem(STORAGE_KEY) || 'en';

    // Store original English text for every translated node
    const originalTexts = new Map();

    // Build a sorted dictionary (longest keys first for greedy replacement)
    const sortedKeys = Object.keys(translations).sort((a, b) => b.length - a.length);

    // ── Translate a single text node ───────────────────
    function translateTextNode(node) {
        if (!node.textContent) return;
        const textContent = node.textContent;
        // Normalize whitespace: replace newlines and multiple spaces with single space
        const normalized = textContent.replace(/\s+/g, ' ').trim();
        if (!normalized) return;

        // Save original on first encounter
        if (!originalTexts.has(node)) {
            originalTexts.set(node, textContent);
        }

        if (currentLang === 'ur') {
            // 1. Try exact match with normalized text
            if (translations[normalized]) {
                node.textContent = translations[normalized].replace(/\?/g, '؟');
                return;
            }

            // 2. Try exact match with raw trimmed text (fallback)
            const trimmed = textContent.trim();
            if (translations[trimmed]) {
                node.textContent = translations[trimmed].replace(/\?/g, '؟');
                return;
            }

            // 3. Try greedy partial match (longest key first)
            // Use normalized text for partial matching too
            let text = normalized;
            let changed = false;
            for (const key of sortedKeys) {
                if (key.length > 3 && text.includes(key)) {
                    text = text.split(key).join(translations[key]);
                    changed = true;
                }
            }
            if (changed) {
                node.textContent = text.replace(/\?/g, '؟');
            } else if (textContent.includes('?')) {
                node.textContent = textContent.replace(/\?/g, '؟');
            }
        } else {
            // Restore English original
            const orig = originalTexts.get(node);
            if (orig !== undefined) node.textContent = orig;
        }
    }

    // ── Walk all text nodes in the DOM ─────────────────
    function getTextNodes(root) {
        const walker = document.createTreeWalker(
            root,
            NodeFilter.SHOW_TEXT,
            {
                acceptNode: function (node) {
                    const parent = node.parentElement;
                    if (!parent) return NodeFilter.FILTER_REJECT;
                    const tag = parent.tagName;
                    if (tag === 'SCRIPT' || tag === 'STYLE' || tag === 'NOSCRIPT') {
                        return NodeFilter.FILTER_REJECT;
                    }
                    if (node.textContent.trim().length === 0) {
                        return NodeFilter.FILTER_REJECT;
                    }
                    return NodeFilter.FILTER_ACCEPT;
                }
            }
        );
        const nodes = [];
        while (walker.nextNode()) nodes.push(walker.currentNode);
        return nodes;
    }

    // ── Translate placeholder & title attributes ───────
    function translateAttributes(root) {
        const attrElements = root.querySelectorAll('[placeholder], [title], [aria-label]');
        attrElements.forEach(el => {
            ['placeholder', 'title', 'aria-label'].forEach(attr => {
                const val = el.getAttribute(attr);
                if (!val) return;
                const storeKey = `__attr_${attr}_${el.id || el.name || Math.random()}`;
                if (currentLang === 'ur') {
                    if (!originalTexts.has(storeKey)) {
                        originalTexts.set(storeKey, { el, attr, val });
                    }
                    if (translations[val]) {
                        el.setAttribute(attr, translations[val].replace(/\?/g, '؟'));
                    } else {
                        // Even if not in dictionary, if we're in Urdu mode, flip the ?
                        const currentVal = el.getAttribute(attr);
                        if (currentVal.includes('?')) {
                            el.setAttribute(attr, currentVal.replace(/\?/g, '؟'));
                        }
                    }
                } else {
                    const orig = originalTexts.get(storeKey);
                    if (orig) {
                        el.setAttribute(attr, orig.val);
                    }
                }
            });
        });
    }

    // ── Translate <option> elements specifically ───────
    function translateOptions(root) {
        const options = root.querySelectorAll('option');
        options.forEach(opt => {
            const text = opt.textContent.trim();
            if (!originalTexts.has(opt)) {
                originalTexts.set(opt, opt.textContent);
            }
            if (currentLang === 'ur') {
                if (translations[text]) {
                    opt.textContent = translations[text].replace(/\?/g, '؟');
                } else if (opt.textContent.includes('?')) {
                    opt.textContent = opt.textContent.replace(/\?/g, '؟');
                }
            } else {
                const orig = originalTexts.get(opt);
                if (orig !== undefined) opt.textContent = orig;
            }
        });
    }

    // ── Main translate function ────────────────────────
    function translatePage() {
        const body = document.body;
        if (!body) return;

        // Set direction & language attribute
        if (currentLang === 'ur') {
            document.documentElement.setAttribute('dir', 'rtl');
            document.documentElement.setAttribute('lang', 'ur');
            body.classList.add('lang-urdu');
        } else {
            document.documentElement.setAttribute('dir', 'ltr');
            document.documentElement.setAttribute('lang', 'en');
            body.classList.remove('lang-urdu');
        }

        // Translate all text nodes
        const textNodes = getTextNodes(body);
        textNodes.forEach(translateTextNode);

        // Translate attributes (placeholders, titles)
        translateAttributes(body);

        // Translate select options
        translateOptions(body);

        // Update toggle button text
        const btn = document.getElementById('lang-toggle');
        if (btn) {
            const label = btn.querySelector('.lang-label');
            if (label) {
                if (currentLang === 'ur') {
                    label.textContent = 'English';
                    label.classList.add('en-text');
                } else {
                    label.textContent = 'اردو';
                    label.classList.remove('en-text');
                }
            }
        }

    }

    // ── Toggle language ────────────────────────────────
    function toggleLanguage() {
        currentLang = currentLang === 'en' ? 'ur' : 'en';
        localStorage.setItem(STORAGE_KEY, currentLang);
        translatePage();
    }

    // ── Initialize ─────────────────────────────────────
    function init() {
        const btn = document.getElementById('lang-toggle');
        if (btn) {
            btn.addEventListener('click', toggleLanguage);
        }

        // If stored language is Urdu, translate on load
        if (currentLang === 'ur') {
            translatePage();
        }

        // Expose translatePage to global scope for dynamic content
        window.noshahi = window.noshahi || {};
        window.noshahi.translatePage = translatePage;
    }

    // Run after DOM is ready
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }
})();
